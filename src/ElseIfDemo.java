
public class ElseIfDemo {

	public static void main(String[] args) {


		int mark =38;
		
		if(mark>=33 && mark<45){
			
			System.out.println("Third");
			
		}
		else if(mark>=45 && mark<60){
			
			System.out.println("Second");
			
		}
       
        else{
        	System.out.println("First");
        }
		
		
		
		
		

	}

}
